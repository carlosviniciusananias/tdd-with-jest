function concatenar(letter1, letter2) {
    return letter1 + letter2;
}

function subtrair(num1, num2) {
    return num1 - num2;
}

function multiplicar(mult1, mult2) {
    return mult1 * mult2;
}

function dividir(div1, div2) {
    return div1 / div2;
}

module.exports = { concatenar, subtrair, multiplicar, dividir }